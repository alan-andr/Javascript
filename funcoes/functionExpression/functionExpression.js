const subtraction = function(a = 0, b = 0) {
    return a - b;
}

let num1 = 5;
let num2 = 3;
let resultSubtraction = subtraction(num1, num2);

console.log('------------- SUBTRAINDO VALORES -------------');
console.log(`${num1} - ${num2} = ${resultSubtraction}`);
console.log('----------------------------------------------');