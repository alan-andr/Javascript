function maxNumber(a, b) {
    return a > b ? a : b
};

const num1 = 4;
const num2 = 10;

console.log('_-_-_-_-_-_-_- Maior número _-_-_-_-_-_-_-');
console.log(`Qual é o maior número entre ${num1} e ${num2}`);
console.log(`Maior número: ${maxNumber(num2, num1)}`);
